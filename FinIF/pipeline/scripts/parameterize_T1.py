def rebuild_prompt(sample):
    """Rebuild the prompt field from context_text + query_text + constraints."""
    parts = []
    # Context
    parts.append(sample["context_text"])
    # Query
    parts.append("")
    parts.append(sample["query_text"])
    # Constraints
    constraints = sample.get("constraints", [])
    if constraints:
        parts.append("")
        parts.append("请在回答时严格遵守以下附加要求：")
        for i, c in enumerate(constraints, 1):
            parts.append(f"（{i}）{c['rendered_text']}")
    return "\n".join(parts)


def parameterize_sample(sample):
    """Replace F6-F12 constraint text with parameterized versions."""
    sample = copy.deepcopy(sample)
    case_id = sample["case_id"]
    case_params = PARAMETERIZED.get(case_id, {})

    modified = False
    for c in sample.get("constraints", []):
        cid = c["id"]
        if cid in case_params:
            c["rendered_text"] = case_params[cid]
            modified = True

    if modified:
        sample["prompt"] = rebuild_prompt(sample)

    return sample


def main():
    # Read all samples
    all_samples = []
    with open(INPUT_PATH) as f:
        for line in f:
            all_samples.append(json.loads(line))

    # Filter and process T1 samples
    t1_samples = []
    t1_count = 0
    parameterized_count = 0
    constraint_count = 0

    for sample in all_samples:
        if sample.get("L1") != "T1":
            continue
        t1_count += 1
        new_sample = parameterize_sample(sample)

        # Count how many constraints were parameterized
        case_id = sample["case_id"]
        case_params = PARAMETERIZED.get(case_id, {})
        for c in sample.get("constraints", []):
            if c["id"] in case_params:
                constraint_count += 1

        if case_params:
            parameterized_count += 1

        t1_samples.append(new_sample)

    # Write output
    with open(OUTPUT_PATH, "w") as f:
        for sample in t1_samples:
            f.write(json.dumps(sample, ensure_ascii=False) + "\n")

    print(f"Processed {t1_count} T1 samples")
    print(f"Parameterized {parameterized_count} samples")
    print(f"Total F6-F12 constraints parameterized: {constraint_count}")
    print(f"Output written to: {OUTPUT_PATH}")

    # Verification: check coverage
    missing = []
    for sample in t1_samples:
        case_id = sample["case_id"]
        for c in sample.get("constraints", []):
            cid = c["id"]
            if cid in ["F6", "F7", "F8", "F9", "F10", "F11", "F12"]:
                if case_id not in PARAMETERIZED or cid not in PARAMETERIZED[case_id]:
                    missing.append((sample["sample_id"], case_id, cid))

    if missing:
        print(f"\nWARNING: {len(missing)} constraints NOT parameterized:")
        for sid, cid, fid in missing:
            print(f"  {sid} ({cid}): {fid}")
    else:
        print("\nAll F6-F12 constraints successfully parameterized!")


if __name__ == "__main__":
    main()
