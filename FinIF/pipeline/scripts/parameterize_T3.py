def rebuild_prompt(sample):
    """Rebuild the prompt field from context_text + query_text + constraints."""
    parts = [sample["context_text"]]
    parts.append("\n")
    parts.append(sample["query_text"])

    constraint_texts = []
    for c in sample["constraints"]:
        constraint_texts.append(c["rendered_text"])

    if constraint_texts:
        parts.append("\n\n请在回答时严格遵守以下附加要求：")
        for i, ct in enumerate(constraint_texts, 1):
            parts.append(f"\n（{i}）{ct}")

    return "".join(parts)


def main():
    target_ids = {"F6", "F7", "F8", "F9", "F10", "F11", "F12"}
    t3_samples = []
    stats = {"total": 0, "constraints_updated": 0, "samples_with_updates": 0}
    warnings = []

    with open(INPUT_PATH, encoding="utf-8") as f:
        for line in f:
            obj = json.loads(line)
            if obj.get("L1") != "T3":
                continue

            stats["total"] += 1
            s = copy.deepcopy(obj)
            case_id = s["case_id"]
            updated = False

            for c in s["constraints"]:
                cid = c.get("id", "")
                if cid not in target_ids:
                    continue

                key = (case_id, cid)
                if key in PARAM_MAP:
                    c["rendered_text"] = PARAM_MAP[key]
                    stats["constraints_updated"] += 1
                    updated = True
                else:
                    warnings.append(f"  {s['sample_id']}: ({case_id}, {cid}) not found")

            if updated:
                stats["samples_with_updates"] += 1
                s["prompt"] = rebuild_prompt(s)

            t3_samples.append(s)

    with open(OUTPUT_PATH, "w", encoding="utf-8") as f:
        for s in t3_samples:
            f.write(json.dumps(s, ensure_ascii=False) + "\n")

    print(f"Done!")
    print(f"  Total T3 samples: {stats['total']}")
    print(f"  Samples with F6-F12 updates: {stats['samples_with_updates']}")
    print(f"  Total constraints updated: {stats['constraints_updated']}")
    print(f"  Output: {OUTPUT_PATH}")

    if warnings:
        print(f"\nWARNINGS ({len(warnings)} missing mappings):")
        for w in warnings:
            print(w)
    else:
        print("\nAll F6-F12 constraints successfully parameterized.")


if __name__ == "__main__":
    main()
