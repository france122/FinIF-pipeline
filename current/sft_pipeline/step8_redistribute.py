def main():
    bm_path = os.path.join(BASE, "benchmark", "benchmark_all.json")
    ec_path = os.path.join(BASE, "benchmark", "eval_config_all.json")

    # ── 1. 加载 ──
    with open(bm_path, encoding="utf-8") as f:
        bm = json.load(f)
    with open(ec_path, encoding="utf-8") as f:
        ec = json.load(f)

    print(f"Before: {len(bm['cases'])} cases, {len(ec['constraints'])} constraints")

    # ── 2. 移除 batch1_real cases ──
    removed = [c for c in bm["cases"] if c["case_id"] in BATCH1_CASE_IDS]
    bm["cases"] = [c for c in bm["cases"] if c["case_id"] not in BATCH1_CASE_IDS]

    removed_constraint_keys = []
    for k in list(ec["constraints"].keys()):
        case_id = k.split("#")[0]
        if case_id in BATCH1_CASE_IDS:
            removed_constraint_keys.append(k)
            del ec["constraints"][k]

    print(f"Removed {len(removed)} cases, {len(removed_constraint_keys)} constraints")

    # ── 3. 添加合成替代 cases ──
    for sc in SYNTHETIC_CASES:
        case_entry = {
            "case_id": sc["case_id"],
            "prompt": sc["context"] + "\n\n---\n任务：\n" + sc["query"],
            "context": sc["context"],
        }
        bm["cases"].append(case_entry)

        for con in sc["constraints"]:
            key = f"{sc['case_id']}#{con['id']}"
            ec["constraints"][key] = {
                "type": con["type"],
                "checker": con["checker"],
                "params": con["params"],
                "description": con["description"],
            }

    print(f"Added {len(SYNTHETIC_CASES)} synthetic cases")
    print(f"After: {len(bm['cases'])} cases, {len(ec['constraints'])} constraints")

    # ── 4. 备份并写入 ──
    shutil.copy2(bm_path, bm_path + ".bak_pre_redistribute")
    shutil.copy2(ec_path, ec_path + ".bak_pre_redistribute")

    with open(bm_path, "w", encoding="utf-8") as f:
        json.dump(bm, f, ensure_ascii=False, indent=2)
    with open(ec_path, "w", encoding="utf-8") as f:
        json.dump(ec, f, ensure_ascii=False, indent=2)

    # ── 5. L2 分布统计 ──
    l2_count = Counter()
    for c in bm["cases"]:
        l2 = c["case_id"].rsplit("-", 1)[0]
        l2_count[l2] += 1
    print("\nL2 distribution after redistribution:")
    total = sum(l2_count.values())
    for k, v in sorted(l2_count.items()):
        print(f"  {k}: {v} ({v/total*100:.1f}%)")
    print(f"  Total: {total}")

    # ── 6. 约束统计 ──
    hard = sum(1 for v in ec["constraints"].values() if v["type"] == "hard")
    soft = sum(1 for v in ec["constraints"].values() if v["type"] == "soft")
    print(f"\nConstraints: {hard} hard + {soft} soft = {len(ec['constraints'])}")


if __name__ == "__main__":
    main()
