def main():
    # Read all samples
    all_samples = []
    with open(INPUT_PATH) as f:
        for line in f:
            all_samples.append(json.loads(line))

    # Filter T1 samples and apply parameterization
    t1_output = []
    param_count = 0
    missing = []

    for sample in all_samples:
        if sample.get("L1") != "T1":
            continue

        sample = copy.deepcopy(sample)
        sid = sample["sample_id"]

        for c in sample["constraints"]:
            cid = c["id"]
            if cid in TARGET_IDS:
                key = (sid, cid)
                if key in PARAM:
                    c["rendered_text"] = PARAM[key]
                    param_count += 1
                else:
                    missing.append(key)

        t1_output.append(sample)

    # Write output
    with open(OUTPUT_PATH, "w") as f:
        for sample in t1_output:
            f.write(json.dumps(sample, ensure_ascii=False) + "\n")

    print(f"Wrote {len(t1_output)} T1 samples to {OUTPUT_PATH}")
    print(f"Parameterized {param_count} constraints")
    if missing:
        print(f"WARNING: {len(missing)} constraints not found in PARAM mapping:")
        for m in missing:
            print(f"  {m}")
    else:
        print("All F6-F12 constraints successfully parameterized!")


if __name__ == "__main__":
    main()
